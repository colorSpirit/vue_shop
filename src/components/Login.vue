<template>
    <div id="login" class="login_container">
        <div class="login_box">
            <!-- 头像区域-->
            <div class="avatar_box">
                <img src="../assets/logo.png" alt="">
            </div>
            <!-- 登录表单区域-->
            <el-form ref="loginForm" :model="loginForm" :rules="loginFormRules" label-width="0" class="login_form">
                <!--用户名区域-->
                <el-form-item prop="username">
                    <el-input placeholder="输入用户名" prefix-icon="iconfont icon-user"
                              v-model="loginForm.username"></el-input>
                </el-form-item>
                <!-- 密码区域-->
                <el-form-item prop="password">
                    <!--验证规则是加给 el-form-item 而不是加给 el-input -->
                    <el-input placeholder="输入密码" prefix-icon="iconfont icon-3702mima" v-model="loginForm.password"
                              type="password"></el-input>
                </el-form-item>
                <!-- 按钮区域-->
                <el-form-item class="btns">
                    <el-button type="primary" @click="validateForm">登录</el-button>
                    <el-button type="info" @click="resetLoginForm">重置</el-button>
                </el-form-item>
            </el-form>
        </div>
    </div>
</template>
<script>
    export default {
        data() {
            return {
                loginForm: {
                    username: '',
                    password: ''
                },
                /*验证规则对象*/
                loginFormRules: {
                    /*验证用户名*/
                    username: [
                        {require: true, message: "请输入用户名", trigger: "blur"},
                        {min: 3, max: 10, message: "长度在3 到 10 个字符之间", trigger: "blur"}
                    ],
                    /*验证密码*/
                    password: [
                        {require: true, message: "请输入密码", trigger: "blur"},
                        {min: 6, max: 15, message: "长度在6到15个字符之间", trigger: "blur"}
                    ]
                }
            }
        },
        methods:{
            /*调用element-ui中 Form的API实现表单重置*/
            resetLoginForm(){
                this.$refs.loginForm.resetFields();
            },
            /*在登录前，调用element-ui中Form的api实现预验证*/
            validateForm(){
                this.$refs.loginForm.validate(async validResult=>{
                    /* validResult位Boolean类型的值 ,验证成功返回true,验证失败返回false*/
                    if(validResult){
                        /*获得响应的结果*/
                      const {data:res}=  await this.$http.post('login',this.loginForm);
                      if(res.meta.status!==200){
                          return  this.$message.error("登录失败");
                      }
                    this.$message.success("登录成功");
                      /*
                      * 1.将登录成功之后的token，保存到客户端的sessionStorage中
                      *     1.1 项目中除了登录之外的其他API接口，必须在登录之后才能访问
                      *     1.2 token只应该在当前网站打开期间生效，所以将token保存到sessionStorage中
                      */
                        window.sessionStorage.setItem('token',res.data.token)
                      /*
                      * 2.通过编程式导航跳转到后台主页，路由地址是/home
                      * */
                     this.$router.push('/home')
                    }
                });

            }
        }
    }
</script>

